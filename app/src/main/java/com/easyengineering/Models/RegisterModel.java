package com.easyengineering.Models;

public class RegisterModel {

    String id, category, firstname, lastname, email, mobile, password, timestamp;

    public RegisterModel(String id, String firstnames, String lastnames, String emails, String mobiles, String passwords, String category, String timestamp) {
        this.id = id;
        this.firstname = firstnames;
        this.lastname = lastnames;
        this.email = emails;
        this.mobile = mobiles;
        this.password = passwords;
        this.category = category;
        this.timestamp = timestamp;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }
}
